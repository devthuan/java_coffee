    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String email = jTextField2.getText();
        String password = String.valueOf(jPasswordField2.getPassword());
        String rePassword = String.valueOf(jPasswordField3.getPassword());

        if (email.equals("") || password.equals("") || rePassword.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin");
            return;
        }

        // // min length paswords
        if (password.length() < 6) {
            JOptionPane.showMessageDialog(this, "Mật khẩu phải lớn hơn 6 ký tự");
            return;
        }

        if (!password.equals(rePassword)) {
            JOptionPane.showMessageDialog(this, "Mật khẩu không trùng khớp");
            return;
        }

        // check email
        boolean check_email = Authen.validateEmail(email);

        if (check_email) {
            AccountDTO new_account = new AccountDTO(email, password);

            if (AccountBUS.createAccountBUS(new_account)) {
                JOptionPane.showMessageDialog(this, "Tạo tài khoản thành công");
                ContentSignUp.setVisible(false);
                ContentLogin.setVisible(true);

                jTextField1.setText(email);
                jPasswordField1.setText(password);

                return;
            }

        }

    }

