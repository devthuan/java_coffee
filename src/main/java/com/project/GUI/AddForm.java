    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {      
        String id = txtMaxhang.getText();     
        String product_name = txtTensp.getText();
        String priceText = txtprice.getText();
        String sp_count = cmbSoluong.getSelectedItem().toString();
        String sp_date = cmbDay.getSelectedItem().toString() + "-" + cmbMonth.getSelectedItem().toString() +"-"+ cmbYear.getSelectedItem().toString();
        String img_path = txtimg.getText();
        
        if (id.isEmpty() || product_name.isEmpty() || priceText.isEmpty()) {
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(this,"Vui lòng nhập Mã hàng");
                txtMaxhang.requestFocus();
                return;
            }
            else {
                try {
                    int mahang ;
                    mahang = Integer.parseInt(id);
                    }
                catch (NumberFormatException e) {
                    // Xử lý khi người dùng nhập vào không phải là số
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập số thích hợp");
                    txtMaxhang.requestFocus();
                    return;
                    }
                }
            if (product_name.isEmpty()) {
                JOptionPane.showMessageDialog(this,"Vui lòng nhập Tên sản phẩm");
                txtTensp.requestFocus();
                return;
            }
            if (priceText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập Giá sản phẩm");
                txtprice.requestFocus();
                return;
            }
            else {
                try {
                    Double price = null;
                    price = Double.parseDouble(priceText);
                    }
                catch (NumberFormatException e) {
                    // Xử lý khi người dùng nhập vào không phải là số
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập số thích hợp");
                    txtprice.requestFocus();
                    return;
                    }
                }

        }

        int rdbtnValue = 0; // Khởi tạo giá trị mặc định

        if (!rdbtnCf.isSelected() && !rdbtnSoda.isSelected() && !rdbtnTrs.isSelected()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn loại sản phẩm");
            return;
        }
        else if (rdbtnCf.isSelected()) {
            rdbtnValue = 1;
        }
        else if (rdbtnTrs.isSelected()) {
            rdbtnValue = 2;
        }
        else {
            rdbtnValue = 3;
        }

        ProductMenu.AddRow(new Object [] {
                                    
            txtMaxhang.getText(),
            txtTensp.getText(),
            txtimg.getText(),
            txtprice.getText(),
            jComboBox6.getSelectedItem(),
            cmbSoluong.getSelectedItem(),
            cmbDay.getSelectedItem().toString()+"-"+cmbMonth.getSelectedItem().toString()+"-"+cmbYear.getSelectedItem().toString(),
            rdbtnValue,
        });
        
        ProductDTO new_product = new ProductDTO(product_name,img_path,Double.parseDouble(priceText),Integer.parseInt(sp_count), rdbtnValue);
        System.out.println(rdbtnValue);
        ProductBUS.addProductBUS(new_product);

    }
    
