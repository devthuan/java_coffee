    public DetailWareHouse(int idWareHouse) {
        initComponents();
        wareHouseService = new WareHouseService();
        try {
            warehouse = wareHouseService.getIdWareHouse(idWareHouse);
            jtfCode.setText(String.valueOf(warehouse.getId()));
            jtfCode.setEnabled(false);
            jtfName.setText(warehouse.getName());
            Timestamp timestamp = warehouse.getCreateDate();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String formattedDateTime = dateFormat.format(timestamp);
            jtfCreate.setText(formattedDateTime);
            jtfCreate.setEnabled(false);
            Timestamp updateTime = warehouse.getUpdateDate();
            String formatUpdate = dateFormat.format(updateTime);
            jtfUpdate.setText(formatUpdate);
            jtfUpdate.setEnabled(false);
        } catch (Exception ex) {
            Logger.getLogger(DetailWareHouse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

